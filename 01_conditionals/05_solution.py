### Suggest activity based on Weather: Sunny (Go for a walk), Rainy (Read a book), Snowy (Build a snowman)

weather = input("Enter weather: ")

if weather == "Sunny":
	print("Go for a walk")
elif weather == "Rainy":
	print("Read a book")
elif weather == "Snowy":
	print("Build a snowman")
else:
	print("No data found")